# Find the length of list and string using functions..

# for list 

def find_list_length(lst):
    return len(lst)
my_list = [1, 2, 3, 4, 5]
list_length = find_list_length(my_list)
print("Length of the list:", list_length)

# for string 
def find_string_length(s):
    return len(s)
my_string = "Hello, world!"
string_length = find_string_length(my_string)
print("Length of the string:", string_length)


